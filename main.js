const add = (firstNumber, secondNumber) => {
    const sum = firstNumber + secondNumber

    return sum
}

const subtract = (firstNumber, secondNumber) => {
    const sum = firstNumber - secondNumber

    return sum
}

const multiply = (firstNumber, secondNumber) => {
    const sum = firstNumber * secondNumber

    return sum
}

const divide = (firstNumber, secondNumber) => {
    const sum = firstNumber / secondNumber

    return sum
}

